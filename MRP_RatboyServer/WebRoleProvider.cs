        public override string[] GetRolesForUser(string username)
        {
            using (var context = new BD_ArmadoPcEntities())
            {
                var result = (from user in context.UsuariosCliente
                              join role in context.UserRole on user.idUserCliente equals role.userIdCliente
                              join userAdmin in context.Usuarios on role.userIdAdmin equals userAdmin.idUsuario
                              where user.correo == username | userAdmin.correo == username
                              select role.rol).ToArray();
                return result;
            }
        }

